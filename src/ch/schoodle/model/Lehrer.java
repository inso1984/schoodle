public class Lehrer{

	private int idLehrer;
	private String nameLehrer;
	
	public int getIdLehrer() {
		return idLehrer;
	}
	public void setIdLehrer(int idLehrer) {
		this.idLehrer = idLehrer;
	}
	public String getNameLehrer() {
		return nameLehrer;
	}
	public void setNameLehrer(String nameLehrer) {
		this.nameLehrer = nameLehrer;
	}
	
	
}
